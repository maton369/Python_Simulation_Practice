# ------------------------------------------------------------
# Python 標準ライブラリ random の各関数を用いた乱数生成デモ
# ------------------------------------------------------------
import random

# ------------------------------------------------------------
# 1. 0〜1 の一様乱数 (float)
# ------------------------------------------------------------
# random.random() は [0.0, 1.0) の範囲で浮動小数点乱数を返す
for i in range(20):
    print("%05.4f" % random.random(), end=" ")
print()  # 改行

# ------------------------------------------------------------
# 2. シード値を固定して再現性のある乱数列を生成
# ------------------------------------------------------------
# random.seed(1) により、同じ順序の乱数が再現される
random.seed(1)
for i in range(20):
    print("%05.4f" % random.random(), end=" ")
print()

# ------------------------------------------------------------
# 3. 指定範囲 [1,100) の一様乱数を生成
# ------------------------------------------------------------
# random.uniform(a, b) は実数範囲 [a, b) から一様分布で抽出
for i in range(20):
    print("%6.4f" % random.uniform(1, 100), end=" ")
print()

# ------------------------------------------------------------
# 4. 整数乱数 (指定範囲内でランダムに抽出)
# ------------------------------------------------------------
# random.randint(a, b) は整数範囲 [a, b] の中からランダムに抽出
for i in range(20):
    print(random.randint(-100, 100), end=" ")
print()

# ------------------------------------------------------------
# 5. 整数乱数（範囲とステップを指定）
# ------------------------------------------------------------
# random.randrange(start, stop, step)
# ここでは 0〜100 の範囲で 5刻み（0,5,10,...,95）の値をランダム生成
for i in range(20):
    print(random.randrange(0, 100, 5), end=" ")
print()

# ------------------------------------------------------------
# 6. リストからランダムに要素を選ぶ
# ------------------------------------------------------------
CitiesList = [
    "Rome",
    "New York",
    "London",
    "Berlin",
    "Moscow",
    "Los Angeles",
    "Paris",
    "Madrid",
    "Tokyo",
    "Toronto",
]

# random.choice() はリストからランダムに1要素を返す
for i in range(10):
    CitiesItem = random.choice(CitiesList)
    print("Randomly selected item from Cities list is -", CitiesItem)

# ------------------------------------------------------------
# 7. リストからランダムサンプリング
# ------------------------------------------------------------
# random.sample(population, k) は母集合から重複なしで k 個抽出する
DataList = range(10, 100, 10)
print("Initial Data List =", list(DataList))
DataSample = random.sample(DataList, k=5)
print("Sample Data List  =", DataSample)
