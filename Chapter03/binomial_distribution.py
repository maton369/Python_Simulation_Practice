# ------------------------------------------------------------
# 二項分布（Binomial Distribution）の乱数生成とヒストグラム表示
# ------------------------------------------------------------
import numpy as np
import matplotlib.pyplot as plt

# ------------------------------------------------------------
# 二項分布のパラメータ設定
# ------------------------------------------------------------
N = 1000  # サンプル数（試行回数）
n = 10  # 各試行における試行回数（例：10回コインを投げる）
p = 0.5  # 成功確率（例：表が出る確率）

# ------------------------------------------------------------
# 二項分布に従う乱数を生成
# ------------------------------------------------------------
# np.random.binomial(n, p, N) は「成功回数」を N 個生成する。
# 各乱数は 0〜n の整数値をとる。
P1 = np.random.binomial(n, p, N)

# ------------------------------------------------------------
# ヒストグラム描画
# ------------------------------------------------------------
plt.figure()
# density=True により確率密度に正規化される（合計が1になる）
plt.hist(P1, density=True, alpha=0.8, histtype="bar", color="green", ec="black")

# 軸ラベル・タイトル設定
plt.title("Binomial Distribution (n=10, p=0.5)")
plt.xlabel("Number of Successes (k)")
plt.ylabel("Probability Density")

# グラフの表示
plt.show()
