# NumPyライブラリをインポート
# np.mod()関数を使用して剰余（mod）演算を行うために利用する
import numpy as np

# ------------------------------------------------------------
# 初期値の設定
# ------------------------------------------------------------
x0 = 1  # 一つ前の値 X_{n-2}
x1 = 1  # 現在の値   X_{n-1}

# 法 (modulus) の設定
# 2^32 = 4294967296。32ビット整数の範囲に収まるようにするため。
m = 2**32

# ------------------------------------------------------------
# フィボナッチ型擬似乱数生成法 (Fibonacci Random Number Generator)
# 一般式: X_n = (X_{n-1} + X_{n-2}) mod m
# ------------------------------------------------------------
for i in range(1, 101):
    # 次の値 X_n を計算
    # 直前の2つの値 x0, x1 の和を法 m で割った余りを次の値とする
    x = np.mod((x0 + x1), m)

    # 次のループのために値を更新
    # 現在の x1 を一つ前の値 x0 に移動し、新しい x を次の x1 に設定
    x0 = x1
    x1 = x

    # 現在生成した値を出力（X_n）
    print(x)
