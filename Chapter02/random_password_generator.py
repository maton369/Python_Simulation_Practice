# ------------------------------------------------------------
# ランダムパスワード生成プログラム
# ------------------------------------------------------------
import string
import random

# ------------------------------------------------------------
# 使用する文字セットを定義
# ------------------------------------------------------------
# 英大小文字 + 数字 + 記号を結合してリスト化
char_set = list(string.ascii_letters + string.digits + "()!$%^&*@#")

# ------------------------------------------------------------
# ユーザーからパスワードの長さを入力してもらう
# ------------------------------------------------------------
password_length = int(input("How long should your password be?: "))

# ------------------------------------------------------------
# 文字セットをシャッフル（順番をランダムに入れ替え）
# ------------------------------------------------------------
random.shuffle(char_set)

# ------------------------------------------------------------
# パスワード生成
# ------------------------------------------------------------
password = []
for i in range(password_length):
    # 文字セットの中から1文字ランダムに選択
    password.append(random.choice(char_set))

# パスワード全体をもう一度シャッフルして順番をさらにランダム化
random.shuffle(password)

# ------------------------------------------------------------
# リストの要素を連結してパスワード文字列を出力
# ------------------------------------------------------------
print("".join(password))
